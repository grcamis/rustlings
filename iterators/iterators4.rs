pub fn factorial(num: u64) -> u64 {
    let mut cont : u64 = 1;
    let mut return_int : u64 = 1;
    if num != 0 {
        while cont <= num.try_into().unwrap() {
            return_int *= cont;
            cont += 1;
        }
    }

    return_int

}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn factorial_of_0() {
        assert_eq!(1, factorial(0));
    }

    #[test]
    fn factorial_of_1() {
        assert_eq!(1, factorial(1));
    }
    #[test]
    fn factorial_of_2() {
        assert_eq!(2, factorial(2));
    }

    #[test]
    fn factorial_of_4() {
        assert_eq!(24, factorial(4));
    }
}
